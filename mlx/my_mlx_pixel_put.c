#include "../include/cub3d.h"

void	my_mlx_pixel_put(t_mlx *mlx, int x, int y, int color)
{
	char	*dst;

	dst = mlx->addr + (y * mlx->line_l + x * (mlx->bpp / 8));
	*(unsigned int*)dst = color;
}
